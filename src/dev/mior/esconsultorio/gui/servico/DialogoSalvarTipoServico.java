    private void initTipoServico() {
        if (tipoServico != null)
            jtfDescricao.setText(tipoServico.getDescricao());
    }
    
    private void showQuitDialog() {
        String mensagem = cadastrar ? "Você realmente deseja cancelar o cadastro do Tipo do Serviço?" : "Você realmente deseja cancelar a edição do Tipo do Serviço?";
        if (jtfDescricao.getText().isEmpty() || JOptionPane.showConfirmDialog(this, mensagem, "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            this.dispose();
        }
    }
    
    private void jbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarActionPerformed
        String descricao = jtfDescricao.getText();
        if (cadastrar) {
            tipoServico = new TipoServico();
        }
        if (descricao.length() < 5) {
            JOptionPane.showMessageDialog(this, "Você deve informar a descrição do Tipo do Serviço!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        } else if ((cadastrar || (tipoServico.getDescricao()!= null && !tipoServico.getDescricao().equals(descricao))) && DAO.findEntityByColumn("descricao", descricao) != null) {
            JOptionPane.showMessageDialog(this, "Um Tipo de Produto já foi cadastrado com esta descrição!", "Erro", JOptionPane.ERROR_MESSAGE);
            return; 
        } else {
            tipoServico.setDescricao(descricao);
        }
                     
        try {
            if (cadastrar) {
                DAO.create(tipoServico);
                JOptionPane.showMessageDialog(this, "Tipo de Serviço cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                DAO.edit(tipoServico);
                JOptionPane.showMessageDialog(this, "Dados do Tipo de Serviço editados com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }
            if (action != null) {
                action.accept(tipoServico);
            }
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao tentar salvar o Tipo de Serviço!", "Erro Desconhecido", JOptionPane.ERROR_MESSAGE);
            Logger.save(e);
        }
    }//GEN-LAST:event_jbtnSalvarActionPerformed

